/* student.c */
/* Файл реализации функций для работы со структурой StudentData */

/* Подключение стандартной библиотеки ввода-вывода */
#include <stdio.h>

/* Подключение заголовочного файла с объявлениями структур и функций */
/* Кавычки "" означают поиск в текущей директории проекта */
#include "student.h"

/*
 * Функция: sstudent
 * Описание: Инициализирует структуру данных студента и выводит информацию
 * Параметры: void (нет входных параметров)
 * Возвращаемое значение: void
 */
void sstudent(void)
{
    /* Объявление переменной student типа struct StudentData */
    /* Эта переменная хранит все данные о студенте в памяти */
    struct StudentData student;
    
    /* =====================================================================
     * ЗАПОЛНЕНИЕ ПОЛЕЙ СТРУКТУРЫ ПЕРСОНАЛЬНЫМИ ДАННЫМИ
     * ===================================================================== */
    
    /* Присваивание имени студента (указатель на строковый литерал) */
    student.stu_name = "Безирген";
    
    /* Присваивание номера студенческого билета (целое число) */
    student.stu_id = 2323438;
    
    /* Присваивание возраста студента */
    student.stu_age = 23;
    
    /* =====================================================================
     * ЗАПОЛНЕНИЕ ВЛОЖЕННОЙ СТРУКТУРЫ stuAddress (тип struct stu_address)
     * ===================================================================== */
    
    /* Улица и номер дома */
    student.stuAddress.street = "ул. Ленина, д. 10, кв. 5";
    
    /* Область/регион */
    student.stuAddress.state = "Московская область";
    
    /* Город */
    student.stuAddress.city = "Москва";
    
    /* Страна */
    student.stuAddress.country = "Russia";
    
    /* =====================================================================
     * ВЫВОД ИНФОРМАЦИИ НА КОНСОЛЬ С ПОМОЩЬЮ printf
     * ===================================================================== */
    
    /* %s — формат спецификатор для строк (char*) */
    /* %d — формат спецификатор для целых чисел (int) */
    /* \n — символ новой строки (перенос строки) */
    
    /* Вывод приветствия с именем студента */
    printf("Hello, %s!\n", student.stu_name);
    
    /* Вывод номера студенческого билета */
    printf("Student Id is: %d\n", student.stu_id);
    
    /* Вывод возраста студента */
    printf("Student Age is: %d\n", student.stu_age);
    
    /* Вывод адреса: страна, регион, город, улица */
    /* Все поля вложенной структуры stuAddress */
    printf("Country: %s, Region: %s, City: %s, Address: %s\n",
           student.stuAddress.country,   /* страна из вложенной структуры */
           student.stuAddress.state,     /* регион из вложенной структуры */
           student.stuAddress.city,      /* город из вложенной структуры */
           student.stuAddress.street);   /* улица из вложенной структуры */
    
    /* Вывод прощального сообщения */
    printf("Goodbye!\n");
    
    /* Конец функции — возврат управления вызывающему коду */
    /* Возврат 0 неявный для функции void, но явно указан для ясности */
}
